#Arithmetic Operator
num1 = 8
num2 = 4

#Adding num1 and num2
print(num1 + num2 )

#Subtracting num2 from num1
print(num1 - num2)

#Multiplying num1 and num2 
print(num1 * num2)

#Divide num1 by num2
print(num1/num2)

#modulus of num1 to num2
print(num1 % num2)

# Raising num1 to the power of num2
print(num1 ** num2)


#Comparison Operator
a = 7
b = 2

#Is a equal to b?
print(a == b)

# is a greater than b?
print(a > b)

#Is a less than or equal to b?
print(a <= b)


# Logical Operators
is_sunny = True
is_rainny = False

#use the and operator to check if both are true
print(is_sunny and is_rainny)

# Use the or opertor to check if one of the condition is true 
print(is_sunny or is_rainny)

#Reversiing the value of is_sunny with not operator
print(not is_sunny)

